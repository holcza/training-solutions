package week15d01;

public class BitcoinOpt {
    private int enter;
    private int exit;

    public BitcoinOpt(int enter, int exit) {
        this.enter = enter;
        this.exit = exit;
    }

    public int getEnter() {
        return enter;
    }

    public int getExit() {
        return exit;
    }
}
